package utils;

/**
 * Contains useful math utility methods
 */
public class Math {

    /**
     * Sum two integers
     *
     * @param a int a
     * @param b int b
     * @return sum of a and b
     */
    public static int Sum(int a, int b) {
        return a + b;
    }

    /**
     * Times method for integers
     *
     * @param a int a
     * @param b int b
     * @return a times b
     */
    public static int times(int a, int b) {
        return a * b;
    }

    public static int divide(int a, int b) {
        return a / b;
    }

}
